#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		ca;

	ca = (char)c;
	while (*s)
	{
		if (*s == ca)
			return ((char*)s);
		s++;
	}
	if (*s == ca)
		return ((char*)s);
	return (NULL);
}
